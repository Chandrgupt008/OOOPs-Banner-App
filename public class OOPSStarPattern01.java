public class OOPSStarPattern01 {
    public static void main(String[] args) {

        // O
        System.out.println(" *****   ");
        System.out.println("*     *  ");
        System.out.println("*     *  ");
        System.out.println("*     *  ");
        System.out.println(" *****   ");

        System.out.println();

        // O
        System.out.println(" *****   ");
        System.out.println("*     *  ");
        System.out.println("*     *  ");
        System.out.println("*     *  ");
        System.out.println(" *****   ");

        System.out.println();

        // P
        System.out.println(" *****   ");
        System.out.println("*     *  ");
        System.out.println(" *****   ");
        System.out.println("*        ");
        System.out.println("*        ");

        System.out.println();

        // S
        System.out.println(" *****   ");
        System.out.println("*        ");
        System.out.println(" *****   ");
        System.out.println("      *  ");
        System.out.println(" *****   ");
    }
}
